    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
         if (isVacio()) {
        getToolkit().beep();
    } else if (esValido()) {
        
        String correo = txtCorreo.getText();
        String usuario = txtUsuario.getText();
        String contra = txtContra.getText();
        String contraEnc=encriptarContrasena(contra);
        String rol = jComboBox1.getSelectedItem().toString();

        UsuarioModelo nuevoUsuario = new UsuarioModelo(correo, usuario, contraEnc, rol);
        UsuarioControlador controlador = new UsuarioControlador();
        boolean registrado = controlador.crearUsuario(nuevoUsuario);

        if (registrado) {
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente.");
            this.dispose(); // Cierra la ventana actual

            // Abre la ventana de Loggin
            Loggin loggin = new Loggin();
            loggin.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "El correo ya está registrado.");
        }
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

