    public boolean esValido(){
        validacion valido = new validacion();
        String Correo = txtCorreo.getText();
        String Contra = txtContra.getText();
        String ContraV = txtContra2.getText();
       
        if(!valido.validarCorreos(Correo)){
            JOptionPane.showMessageDialog(null, "Correo electronico invalido");
            return false;
        }
        
        if(!valido.validarContrasena(Contra)){
            JOptionPane.showMessageDialog(null, "Verifique que la contraseña tenga al menos una minuscula, una mayuscula, un numero,  un caracter especial y que sea de 8 digitos");
            return false;
        }
        
        if(!Contra.equals(ContraV)){
            JOptionPane.showMessageDialog(null, "Verifique la contraseña");
            return false;
        }
        return true;
    }
    private String encriptarContrasena(String contrasena) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hash = md.digest(contrasena.getBytes());
            StringBuilder sb = new StringBuilder();
            for (byte b : hash) {
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("Error al encriptar la contraseña", e);
        }
    }

    
    private boolean verificarContrasena(String contrasena, String contrasenaHash) {
        return encriptarContrasena(contrasena).equals(contrasenaHash);
    }
    
